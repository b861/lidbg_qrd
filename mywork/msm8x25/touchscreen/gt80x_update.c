/*	I2C数据格式为： 7bit从机地址｜读写位 + buf（数据地址+读/写数据）
*	 ------------------------------------------------------------------
*	｜  从机地址   ｜ buf[0](数据地址) | buf[1]~buf[MAX](写入或读取到的数据)  |
*	 ------------------------------------------------------------------
*/
static int mtk_i2c_write_bytes(uint8_t slave_add,  uint8_t *buf, uint16_t len)
{
	int ret = -1;
	uint16_t offset = 0, count = 0;	//
	uint8_t buffer[MAX_I2C_BYTES_NUM+1] = { *buf };	//首地址赋值
	uint8_t * start_reg = buf+1;

	if(len == 0)
		return -EINVAL;

	len--;	//delete data address
	do {
		count = len - offset;
		count = (count>MAX_I2C_BYTES_NUM) ? MAX_I2C_BYTES_NUM : count;
		memcpy(buffer+1, start_reg, count);
		ret = i2c_write_bytes(slave_add, buffer, count+1);
		if(ret <= 0)
			break;
			
		buffer[0] += MAX_I2C_BYTES_NUM;
		start_reg += MAX_I2C_BYTES_NUM;
		offset += MAX_I2C_BYTES_NUM;
	}while(len > offset);
	
	return ret;
}

static int mtk_i2c_read_bytes(uint8_t slave_add,  uint8_t *buf, uint32_t len)
{
	int ret = -1;
	uint16_t offset = 0, count = 0;	//
	uint8_t buffer[MAX_I2C_BYTES_NUM+1] = { *buf };
	uint8_t * start_reg = buf+1;

	if(len == 0)
		return -EINVAL;

	len--;	//delete data address
	do {
		count = len - offset;
		count = (count>MAX_I2C_BYTES_NUM) ? MAX_I2C_BYTES_NUM : count;
		ret = i2c_read_bytes(slave_add, buffer, count+1);
		if(ret <= 0)
			break;
			
		memcpy(start_reg, buffer+1, count);
		buffer[0] += MAX_I2C_BYTES_NUM;
		start_reg += MAX_I2C_BYTES_NUM;
		offset += MAX_I2C_BYTES_NUM;
	}while(len > offset);
	
	return ret;
}

