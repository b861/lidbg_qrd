/*Function as i2c_master_recv
  The first element of buf is the data address. 
 */
static int i2c_read_bytes(uint8_t slave_add,  uint8_t *buf, uint16_t len)
{
	struct i2c_msg msgs[2];
	int ret=-1;

	if(len > (MAX_I2C_BYTES_NUM+1))
	{
		debug_printk(LEVEL_ERROR, "The length of bytes is out of bound.\n");
		return -EINVAL;		
	}
	
	msgs[0].flags = !I2C_M_RD;
	msgs[0].addr=slave_add;
	msgs[0].len=1;
	msgs[0].buf=buf;

	msgs[1].flags = I2C_M_RD;
	msgs[1].addr=slave_add;
	msgs[1].len=len-1;
	msgs[1].buf= buf+1;
	ret=i2c_transfer(i2c_get_adapter(1), msgs, 2);

	return ret;
}

/*Function as i2c_master_send 
  The first element of buf is the data address. 
 */
static int i2c_write_bytes(uint8_t slave_add,  uint8_t *buf, uint16_t len)
{
	struct i2c_msg msg;
	int ret=-1;
	
	if(len > (MAX_I2C_BYTES_NUM+1))
	{
		debug_printk(LEVEL_ERROR, "The length of bytes is out of bound.\n");
		return -EINVAL;
	}
	
	msg.flags = !I2C_M_RD;	
	msg.addr=slave_add;
	msg.len=len;
	msg.buf=buf;		
	ret=i2c_transfer(i2c_get_adapter(1), &msg, 1);

	return ret;
}
