//擦除一段EEPROM
static bool gt80x_erase_eeprom(void)
{
	int ret;
	int i,j;
	for(i =0; i < (sizeof(gt80x_eeprom_code) + 1) / 2; i++)
	{
		ret = 0;
		for(j = 0; j < 5; j++)
			ret += gt80x_erase_addr(gt80x_to_write_addr(0x4000 + 2 * i));

		if(ret == 0)
		{
			debug_printk(LEVEL_ERROR, "Erase addr 0x%04x failed.\n", 0x4000+2*i);
			return false;
		}
	}

	return true;
}

